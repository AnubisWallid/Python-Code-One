##### Operadores Matemáticos

'''
Soma: +
Mutiplicação: *
Divisão: /
Divisão inteira: //
Resto da divisão: %
Potência: **
'''
numero1 = 10
numero2 = 20
print(numero1 + numero2)# 30
print(numero1 * numero2)# 200
print(numero1 / numero2)# 0.5
print(numero1 // numero2)# 0 // descarta a parte fracionaria
print(numero1 % 2)# 0 
print(numero1 ** numero2)# 100000000000000000000 // kkk

### Operadores Comparação

'''
maior que: >
maior ou igual: >=
menor: <
menor ou igual: <=
igual a: ==
diferente de: !=
'''

print(numero1 > numero2)#False
print(numero1 >= numero2)#False
print(numero1 < numero2)#True
print(numero1 <= numero2)#True
print(numero1 == numero2)#False
print(numero1 != numero2)#True
print("maça" == "maça")#True
print("maça" != "pera")#True